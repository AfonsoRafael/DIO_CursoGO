// Abstração em Go: Usando funções para simplificar operações complexas

// Parametros em GO: são passadas por uma variável ou valor direto na chamada da função
// e a transformamos em uma sub-rotina

// Estruturas em GO: é semelhante a outras linguagens, usamos struct para agrupar dados relacionados, objetos

// Funções em GO: são blocos de código reutilizáveis que realizam tarefas específicas, definidas com a palavra-chave func

// Sistemas de tipos em GO: é uma linguagem de tipagem estática, onde o tipo de cada variável é conhecido em tempo de compilação, conjunto de regras que definem como os tipos de dados são usados e interagem

// Pacotes em GO: são unidades de código reutilizáveis que agrupam funcionalidades relacionadas, facilitando a organização e modularização do código

// Interfaces em GO: são tipos que definem um conjunto de métodos que um tipo concreto deve implementar, permitindo a abstração e o polimorfismo

// Métodos em GO: são funções associadas a tipos específicos, permitindo que os tipos tenham comportamentos definidos

// Exceções em GO: são tratadas usando os mecanismos de panic e recover, permitindo lidar com erros em tempo de execução de forma controlada